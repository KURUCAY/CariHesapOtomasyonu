    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        KullaniciProperty kp = new KullaniciProperty();
        kp.setkAdi(txtAdKontrol.getText());
        kp.setkSifre(txtSifreKontrol.getText());
        try {
            String query = "select * from kullanicilar ";
            ResultSet rs = db.baglan().executeQuery(query);
            while (rs.next()) {
                //şifre ve kadi eşleşmeleri ayrı ayrı kontrol edip daha efektif yapılabilir
                if ((kp.getkAdi().equals(rs.getString("kAd")) && kp.getkSifre().equals(rs.getString("kSifre")))) {
                    kullaniciNo = rs.getString("kNo");
                    kullaniciAdi = rs.getString("kAd");
                    txtYeniSifre.setEnabled(true);
                    txtYeniSifreTekrar.setEnabled(true);

                }
            }
        } catch (SQLException e) {
            System.err.println("kullanici kontrol ederken hata! " + e);
        } finally {
            db.kapat();
        }


    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        KullaniciProperty kp = new KullaniciProperty();
        if (txtYeniSifre.getText().equals(txtYeniSifreTekrar.getText())) {
            try {
                String query = "update kullanicilar set kSifre='" + txtYeniSifreTekrar.getText() + "' where kNo='" + kullaniciNo + "'";
                int degistirmeSonuc = db.baglan().executeUpdate(query);
                if (degistirmeSonuc > 0) {
                    JOptionPane.showMessageDialog(rootPane, "şifreniz değiştirildi sn:  " + kullaniciAdi);
                    txtAdKontrol.setText("");
                    txtSifreKontrol.setText("");
                    txtYeniSifre.setText("");
                    txtYeniSifreTekrar.setText("");
                }
            } catch (HeadlessException | SQLException e) {
                System.err.println("Şifre Değiştirirken Hata! " + e);
            } finally {
                db.kapat();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "şifreler uyuşmuyor!");
            txtYeniSifreTekrar.requestFocus();
        }


    }//GEN-LAST:event_jButton3ActionPerformed

