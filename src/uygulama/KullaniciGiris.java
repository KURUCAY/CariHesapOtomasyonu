    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        //kayıt ad soy giriş adsoylar için validatorler metodlaştırılacak!!!!
        String adi = txtAdKayit.getText().trim();
        String sifre = txtSifreKayit.getText().trim();

        kp.setkAdi(adi);
        kp.setkSifre(sifre);

        try {
            String query = "insert into kullanicilar  values(null,'" + kp.getkAdi() + "','" + kp.getkSifre() + "')";
            int i = db.baglan().executeUpdate(query);
            JOptionPane.showMessageDialog(rootPane, "Kayıt işlemi gerçekleştirildi.Giriş yapınız.");
            jTabbedPane1.setSelectedIndex(0);
            txtKullaniciAd.setText("");
            txtKullaniciAd.requestFocus();
            txtKullaniciSifre.setText("");

        } catch (SQLException ex) {

            System.out.println("kayıt hatası: " + ex);
            if (ex.toString().contains("SQLITE_CONSTRAINT_UNIQUE")) {
                JOptionPane.showMessageDialog(rootPane, "aynı kullanıcı ismi zaten var!");
                txtAdKayit.setText("");
                txtAdKayit.requestFocus();
            }
        } finally {
            db.kapat();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

